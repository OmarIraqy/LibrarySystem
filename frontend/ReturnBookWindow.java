    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (studentId.getText().isBlank() || bookId.getText().isBlank() || returnDate.getDate() == null) {
            JOptionPane.showMessageDialog(null, "Some fields are empty");
        }else
        {
            JOptionPane.showMessageDialog(null, "The Student with id = "+studentId.getText()+" should pay a return fee of "+ LibrarianRoleWindow.librarian.returnBook(studentId.getText(), bookId.getText(), returnDate.getDate().toInstant().atZone(ZoneId.systemDefault()).toLocalDate()) +" US dollars for the book with id = "+bookId.getText());
            studentId.setText("");
            bookId.setText("");
            returnDate.setDate(null);
            ((JFrame) getParentNode()).setVisible(true);
            this.setVisible(false);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

